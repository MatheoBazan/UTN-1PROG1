#¿Existen variables tipo valor y referencia en Python?
#En Python, todo son objetos. No existe una diferencia estricta como en Java (valor vs. referencia).
#Para tipos simples (números, strings, booleanos), cuando uno los asigna, pareciera que son "tipo valor" porque se copian.
#Para listas, diccionarios, objetos, se manejan como referencias, es decir, si cambia uno, también cambia el otro si apuntan al mismo objeto.

#Podriamos separarlos en inmutables y mutables
#Inmutables (int, str, tuple) → como valor.
#Mutables (list, dict, objetos) → como referencia.
